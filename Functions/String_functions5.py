# Strings Functions

# Strip Function return a copy of the string with leading and trailing whitespace removed
print("    This is a sentance. ".strip())

# Swapcase is used to convert uppercase characters to lowercase and lowercase characters to uppercase
print("THIS is a sentence.".swapcase())

# Casefold is used to return a version of the string suitable for caseless comparisons
print("This is a senTANCE. This is ANOTHER sentance.".casefold())