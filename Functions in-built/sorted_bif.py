# Sort a tuple:

a = ("b", "g", "a", "d", "f", "c", "h", "e")
x = sorted(a)
print(x)

# Sort numeric:

a = (1, 11, 2)
x = sorted(a)
print(x)

# Sort ascending:

a = ("h", "b", "a", "c", "f", "d", "e", "g")
x = sorted(a)
print(x)

# Sort descending:

a = ("h", "b", "a", "c", "f", "d", "e", "g")
x = sorted(a, reverse=True)
print(x)
